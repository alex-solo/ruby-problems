class CircularQueue
  attr_accessor :queue, :buffer_size

  def initialize(buffer_size)
    @buffer_size = buffer_size
    @queue = []
  end

  def enqueue(obj)
    full? ? queue.push(obj).delete_at(0) : queue << obj
  end

  def dequeue
    queue.delete_at(0)
  end

  def full?
    queue.size == buffer_size
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
